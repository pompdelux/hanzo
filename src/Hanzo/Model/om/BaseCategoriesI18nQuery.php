    public function prune($categoriesI18n = null)
    {
        if ($categoriesI18n) {
            $this->addCond('pruneCond0', $this->getAliasedColName(CategoriesI18nPeer::ID), $categoriesI18n->getId(), Criteria::NOT_EQUAL);
            $this->addCond('pruneCond1', $this->getAliasedColName(CategoriesI18nPeer::LOCALE), $categoriesI18n->getLocale(), Criteria::NOT_EQUAL);
            $this->combine(array('pruneCond0', 'pruneCond1'), Criteria::LOGICAL_OR);
        }

        return $this;
    }

